def load_from_file(file_name: str) -> List[int]:
    try:
        file = open(file_name, "rt")
        s: str = file.read()
        str_list: List[str] = s.split(",")
        num_list: List[int] = []
        for i in range(0, len(str_list)):
            num_list.append(int(str_list[i]))

        return num_list
    except OSError:
        print("Невозможно открыть файл")
        return []

